export function createFont(scene: Phaser.Scene): void {
  const rows = Math.ceil(FONT_CHARS.length / PER_ROW);
  const tex = scene.textures.createCanvas(FONT_KEY, PER_ROW * CHAR_W, rows * CHAR_H)!;
  const ctx = tex.getContext();
  ctx.fillStyle = '#ffffff';
  for (let i = 0; i < FONT_CHARS.length; i++) {
    const glyph = G[FONT_CHARS[i]];
    if (!glyph) continue;
    const cx = (i % PER_ROW) * CHAR_W;
    const cy = Math.floor(i / PER_ROW) * CHAR_H;
    for (let y = 0; y < 7; y++) {
      for (let x = 0; x < 5; x++) {
        if (glyph[y][x] === '1') ctx.fillRect(cx + x, cy + y, 1, 1);
      }
    }
  }
  tex.refresh();
  const cfg: Phaser.Types.GameObjects.BitmapText.RetroFontConfig = {
    image: FONT_KEY,
    width: CHAR_W,
    height: CHAR_H,
    chars: FONT_CHARS,
    charsPerRow: PER_ROW,
    'spacing.x': 0,
    'spacing.y': 0,
    lineSpacing: 2,
    'offset.x': 0,
    'offset.y': 0
  };
  scene.cache.bitmapFont.add(FONT_KEY, Phaser.GameObjects.RetroFont.Parse(scene, cfg));
}
